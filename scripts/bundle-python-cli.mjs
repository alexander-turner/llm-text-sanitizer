#!/usr/bin/env node
/**
 * Bundle the sanitizer CLI into a single self-contained `.mjs` for the Python
 * wheel.
 *
 * The Python client (`python/agent_input_sanitizer`) shells out to the Node
 * CLI. So `pip install` alone works — with only Node on PATH, no separate
 * JavaScript checkout and no `AGENT_SANITIZER_CLI` — this bundles
 * `bin/sanitize-cli.mjs` and everything it imports (the `src/` logic AND its npm
 * dependencies: remark/rehype/unified for the HTML layers) into ONE file with no
 * `node_modules` requirement, written into the Python package so the wheel ships
 * it.
 *
 * This is a BUILD ARTIFACT, generated at release time from `src/` at the current
 * version — not a hand-maintained port. There is exactly one source of truth
 * (`src/`) and one version per build, so it cannot drift the way a vendored copy
 * would. It is git-ignored and regenerated by the publish workflow; hatchling
 * force-includes it via `[tool.hatch.build] artifacts` (see python/pyproject.toml).
 *
 * Run from the repo root: `node scripts/bundle-python-cli.mjs`.
 */
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

import { build } from "esbuild";

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), "..");
const entry = join(repoRoot, "bin", "sanitize-cli.mjs");
const outfile = join(
  repoRoot,
  "python",
  "agent_input_sanitizer",
  "_bundled",
  "sanitize-cli.mjs",
);

await build({
  entryPoints: [entry],
  outfile,
  bundle: true,
  // Target the Node runtime the package requires (>=22): keeps Node built-ins
  // (node:fs, node:buffer, …) external and emits modern syntax the CLI relies on
  // (top-level await, dynamic import).
  platform: "node",
  format: "esm",
  target: "node22",
  // esbuild preserves `import.meta.url` under platform:node/format:esm, which
  // the CLI compares against process.argv[1] to decide whether it was invoked as
  // a script — so the bundled CLI still runs its worker/one-shot modes.
  logLevel: "info",
});
